public enum EnumProjectPushType {
    /**
     * 添加操作
     */
    ADD("add"),
    /**
     * 更新操作
     */
    UPDATE("update"),
    /**
     * 删除操作
     */
    DELETE("delete");
    /**
     * 常量
     */
    private  String val="";
    /**
     * 构造方法
     * @param value
     */
    private EnumProjectPushType(String value) {
       this.val=value;
    }

    /**
     * 获取val
     * @return
     */
    public String getVal(){
        return val;
    }
}
