package net.evecom.platform.bdc.enumbdc;
/**
 * 描述   签署公章类型
 * @author Rider Chen
 * @version 1.0
 * @created 2014年9月11日 上午9:13:33
 */
public enum SignIdentity {
    /**
     * 个人公章
     */
    PERSON("PERSON"),
    /**
     * 企业公章
     */
    ORGANIZE("ORGANIZE"),
    /**
     * 公司法人公章
     */
    LEGAL("LEGAL"),
    /**
     * 不限制;多个用
     */
    NOLIMIT("NOLIMIT");
    /**
     * 常量
     */
    private  String val="";
    /**
     * 构造方法
     * @param value
     */
    private SignIdentity(String value) {
       this.val=value;
    }

    /**
     * 获取val
     * @return
     */
    public String getVal(){
        return val;
    }
}
